    private void userSearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userSearchBtnActionPerformed
        // TODO add your handling code here:
        
        String userName = searchUserTxt.getText();
        
                try {
            Connection con =ConectionDB.getCon();
            String sql = "SELECT * FROM addusers WHERE username = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, userName);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                userRoleTxt.setText(rs.getString("userRole"));
                userNameTxt.setText(rs.getString("name"));
                userAddressTxt.setText(rs.getString("address"));
                userEmailTxt.setText(rs.getString("email"));
                userMobileTxt.setText(rs.getString("mobileNumber"));
            } else {
                JOptionPane.showMessageDialog(null, "User not found.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error retrieving user data: " + e.getMessage());
        }
    }//GEN-LAST:event_userSearchBtnActionPerformed

