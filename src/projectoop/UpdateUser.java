    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String userName = searchUserTxt.getText();
        
        
        //assign updated field values 
        String userRoleUp = userRoleTxt.getText();
        String nameUp  = userNameTxt.getText();
        String mobileNumberUp = userMobileTxt.getText();
        String emailUp  = userEmailTxt.getText();
        String addressUp  = userAddressTxt.getText();
        
        
        //include Updated values for hashmap
        HashMap <String,String> updateUserMap = new HashMap<>();
        
        updateUserMap.put("userRoleUpdated", userRoleUp);
        updateUserMap.put("nameUpdated", nameUp);
        updateUserMap.put("mobileNumberUpdated", mobileNumberUp);
        updateUserMap.put("emailUpdated", emailUp);
        updateUserMap.put("addressUpdated", addressUp);
        
        
         //Check Validation
        
        if(nameUp.equals("")){
            JOptionPane.showMessageDialog(null, "Name field is required.");
        }else if(mobileNumberUp.equals("")){
            JOptionPane.showMessageDialog(null, "Mobile Number field is required.");
        }else if(!mobileNumberUp.matches(mobileNumberPattern)|| mobileNumberUp.length() != 10){
            JOptionPane.showMessageDialog(null, "Mobile Number field is invalid.");
        }else if (emailUp.equals("")){
            JOptionPane.showMessageDialog(null, "Email field is required.");
        }else if (!emailUp.matches(emailPattern)){
            JOptionPane.showMessageDialog(null, "Email field is invalid.");
        }else if (addressUp.equals("")){
            JOptionPane.showMessageDialog(null, "Address field is required.");
        }else{};
        
        
        try{
            con = ConectionDB.getCon();
            String query = "UPDATE addusers SET userRole=?, name=?, mobileNumber=?, email=?, address=? WHERE username='"+userName+"'" ;
            pst = con.prepareStatement(query);
            
            for (Map.Entry<String, String> entry : updateUserMap.entrySet()) {
                String key = entry.getKey();
                String value = entry.getValue();
                
                // Here, you may need to map each key to its corresponding column in the database table
                switch (key) {
                    case "userRoleUpdated":
                        pst.setString(1, userRoleUp);
                        break;
                    case "nameUpdated":
                        pst.setString(2, nameUp);
                        break;
                    case "mobileNumberUpdated":
                        pst.setString(3, mobileNumberUp);
                        break;
                    case "emailUpdated":
                        pst.setString(4, emailUp);
                        break;
                    case "addressUpdated":
                        pst.setString(5, addressUp);
                        break;
                    default:
                       
                        break;
                }
            }
            
            // Execute the prepared statement
             pst.executeUpdate();
             JOptionPane.showMessageDialog(null , "Update User SUCCESSFULLLY !!!");
             
            
            setVisible(false);
            new UpdateUser().setVisible(true);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null , e);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

